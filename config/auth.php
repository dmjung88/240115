    |
    | 모든 인증 드라이버에는 사용자 제공자가 있습니다. 이것은 다음과 같은 방법을 정의합니다
    | 사용자는 실제로 데이터베이스 또는 다른 스토리지에서 검색됩니다
    | 사용자의 데이터를 유지하기 위해 이 응용프로그램에서 사용하는 메커니즘입니다.
    |
    | Supported: "session"
    |
    */

    'guards' => [
        'web' => [
            'driver' => 'session',
            'provider' => 'users',
        ],
    ],

    /*
    |--------------------------------------------------------------------------
    | User Providers
    |--------------------------------------------------------------------------
    |
    |
