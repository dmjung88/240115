    | 필요에 따라 대부분의 애플리케이션에서 완벽한 시작입니다.
    |
    */

    'defaults' => [
        'guard' => 'web',
        'passwords' => 'users',
    ],

    /*
    |--------------------------------------------------------------------------
    | Authentication Guards
    |--------------------------------------------------------------------------
    |
    | 다음으로 애플리케이션에 대한 모든 인증 가드를 정의할 수 있습니다.
    | 물론 훌륭한 기본 구성이 정의되었습니다
